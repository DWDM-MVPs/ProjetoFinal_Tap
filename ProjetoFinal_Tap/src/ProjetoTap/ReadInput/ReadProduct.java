    public static int productStock(boolean notNull)
    {
        System.out.println(Lang.readProductStock);
        try
        {
            int number = sc.nextInt();

            if (number < 0 && !notNull)
            {
                errorReset(Lang.errorReadLowProductStock);
                return productStock(false);
            }
            else if (number <= 0 && notNull)
            {
                errorReset(Lang.errorReadLowOrNullProductStock);
                return productStock(true);
            }

            return number;
        }
        catch (Exception ignored)
        {
            errorReset(Lang.errorWrongDataType);
            return productStock(notNull);
        }
    }
